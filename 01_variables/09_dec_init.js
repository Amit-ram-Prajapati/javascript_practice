// What is the difference between variable declaration and variable initialization?
// Answer: Variable declaration is the process of creating a variable, while variable initialization is the process of assigning a value to a variable.

let x; // variable declaration
x = 10; // variable initialization
console.log(x); // 10

// What value does JavaScript implicitly assign to uninitialized var variables?
var y;
console.log(y); // undefined

// What value does JavaScript implicitly assign to uninitialized let variables?
let z;
console.log(z); // undefined

// True or False: let x = undefined; is a valid statement.
// Answer: True